public class Tokens {
    private int Wilds;
    private int Wastes;
    private int DevA;
    private int DevB;
    
    public Tokens() {}
    
    public int getWilds() { return Wilds; }
    public void setWilds(int wilds) { Wilds = wilds; }
    
    public int getWastes() { return Wastes; }
    public void setWastes(int wastes) { Wastes = wastes; }
    
    public int getDevA() { return DevA; }
    public void setDevA(int devA) { DevA = devA; }
    
    public int getDevB() { return DevB; }
    public void setDevB(int devB) { DevB = devB; }
}
